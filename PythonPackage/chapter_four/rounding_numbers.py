def rounding_integer():
    return round(13.56449)


def rounding_to_the_nearest_tenth():
    return round(13.56449, 0)


def rounding_to_the_nearest_hundredth():
    return round(13.56449, 1)


def rounding_to_the_nearest_thousandth():
    return round(13.56449, 2)


print(rounding_integer())
print(rounding_to_the_nearest_tenth())
print(rounding_to_the_nearest_hundredth())
print(rounding_to_the_nearest_thousandth())
